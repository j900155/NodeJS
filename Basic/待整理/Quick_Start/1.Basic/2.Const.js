//const為增強定義 在定義後不能改變值
//另外在Coding Style中記得全為大寫
const APPLE=7;            //增強定義APPLE
console.log(APPLE);       //秀出APPLE
APPLE= 8                  //將值8放入APPLE
console.log(APPLE);       //秀出APPLE
const BANANA;             //增強定義BANANA
console.log(BANANA);      //秀出BANANA
//---------------------------
/*執行結果
7
7
undefined
*/
